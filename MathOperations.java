public class MathOperations {
    //add method
    public int add(int a, int b){
        return a+b;
    }

    //sub method
    public int sub(int a, int b){
        return a-b;
    }

    // mul method
    public long mul(int a, int b){
        return a*b;
    }

    //div method
    public int div(int a, int b){
        return a/b;
    }

}
